class ArithOps{
    public static void main(String[] args){
        short x=6;
        int y=4;
        float a=12.5f;
        float b=7.2f;

        System.out.println("x is " + x + " and y is " + y);
        System.out.println("x+y is " + (x+y));
        System.out.println("x-y is " + (x-y));
        System.out.println("x*y is " + (x*y));
        System.out.println("x/y is " + (x/y));
        System.out.println("x%y is " + (x%y));
        System.out.println("x++ is " + (x++));
        System.out.println("x-- is " + (x--));
        System.out.println("++x is " + (++x));
        System.out.println("--x is " + (--x));
        System.out.println("a is " + a + " and b is " + b);
        System.out.println("a+b is " + (a+b));
        System.out.println("a-b is " + (a-b));
        System.out.println("a*b is " + (a*b));
        System.out.println("a/b is " + (a/b));
        System.out.println("a%b is " + (a%b));
    }
}